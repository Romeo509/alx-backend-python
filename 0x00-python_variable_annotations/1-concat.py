#!/usr/bin/env python3
"""
This module contains the concat function.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings.

    Parameters:
    str1 (str): The first string.
    str2 (str): The second string.

    Returns:
    str: The concatenated string.
    """
    return str1 + str2
